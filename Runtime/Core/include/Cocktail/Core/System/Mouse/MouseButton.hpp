#ifndef COCKTAIL_CORE_SYSTEM_MOUSE_MOUSEBUTTON_HPP
#define COCKTAIL_CORE_SYSTEM_MOUSE_MOUSEBUTTON_HPP

namespace Ck
{
    /**
     * \brief Enumeration of supported buttons of the mouse
     */
    enum class MouseButton
    {
        /**
         * \brief Left mouse button
         */
        Left,

        /**
         * \brief Middle mouse button
         */
        Middle,

        /**
         * \brief Right mouse button
         */
        Right,

        /**
         * \brief Extra1 mouse button
         */
        Extra1,

        /**
         * \brief Extra2 mouse button
         */
        Extra2,
    };
}

#endif // COCKTAIL_CORE_SYSTEM_MOUSE_MOUSEBUTTON_HPP